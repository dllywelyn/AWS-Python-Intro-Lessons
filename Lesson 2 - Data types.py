
my_value = 1                    # create an integer as a variable
print(my_value)                 # print variable
print(type(my_value), "\n")     # print variable's data type

# create variable of integer data type together as strings 
# (with another string between)
type_info = (str(my_value) + " is of the data type " + str(type(my_value)))
print(type_info)                # print above statement    
print(type(type_info))          # print statement's data type